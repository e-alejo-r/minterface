package edg.android.minterface.rsize

/**
 * Enum class representing the different types of window sizes.
 */
enum class WindowType {
    /**
     * Represents a compact window size.
     */
    COMPACT,

    /**
     * Represents a medium window size.
     */
    MEDIUM,

    /**
     * Represents an expanded window size.
     */
    EXPANDED
}
